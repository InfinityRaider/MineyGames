package com.infinityraider.miney_games.core;

public class MineyGameSize {
    private final int width;
    private final int depth;

    public MineyGameSize(int width, int depth) {
        this.width = width;
        this.depth = depth;
    }

    public int getWidth() {
        return this.width;
    }

    public int getDepth() {
        return this.depth;
    }
}
